// Function to get featured posts (just using the newest ones for this example)
export const getFeaturedPosts = () => {
  // Sort posts by date (newest first) and take the first 3
  return [...blogPosts]
    .sort((a, b) => new Date(b.date) - new Date(a.date))
    .slice(0, 3);
};

// Function to get a single post by slug
export const getPostBySlug = (slug) => {
  return blogPosts.find(post => post.slug === slug);
};

// Function to search posts
export const searchPosts = (query) => {
  const lowerCaseQuery = query.toLowerCase();
  
  return blogPosts.filter(post => {
    // Search in title, content, excerpt, and tags
    return (
      post.title.toLowerCase().includes(lowerCaseQuery) ||
      post.excerpt.toLowerCase().includes(lowerCaseQuery) ||
      post.content.toLowerCase().includes(lowerCaseQuery) ||
      post.tags.some(tag => tag.toLowerCase().includes(lowerCaseQuery))
    );
  });
};

// Function to get all unique tags
export const getAllTags = () => {
  const allTags = blogPosts.flatMap(post => post.tags);
  return [...new Set(allTags)].sort();
};

// Function to get posts by tag
export const getPostsByTag = (tag) => {
  return blogPosts.filter(post => 
    post.tags.some(t => t.toLowerCase() === tag.toLowerCase())
  );
};

// Get recent posts sorted by date
export const getRecentPosts = () => {
  return [...blogPosts]
    .sort((a, b) => new Date(b.date) - new Date(a.date))
    .slice(0, 6);
};
